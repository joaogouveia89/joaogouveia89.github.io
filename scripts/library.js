function solutionsCompare(sol1, sol2){
	return sol1.problemName.localeCompare(sol2.problemName);
}

function generateSolutionsList(){
	let unorderedList = document.getElementById("solutions-list");

	//solution
	for(var i = 0; i < solutions.length; i++){

		var cardDiv = document.createElement("div");
		cardDiv.classList.add("card");

		var rowDiv = document.createElement("div");
		rowDiv.classList.add("row");

		var colMd3 = document.createElement("div");
		colMd3.classList.add("col-md-3");
		colMd3.classList.add("bg-primary"); //HERE WE DEFINE THE BACKGROUND OF THE LEFT PART OF THE LIST ITEM

		var solutionHeaderDiv = document.createElement("div");
		solutionHeaderDiv.classList.add("card-body");
		solutionHeaderDiv.classList.add("cc-problem-solution-header");

		var platformP = document.createElement("p");
		platformP.insertAdjacentHTML('afterbegin', solutions[i].platform);

		var problemNameDiv = document.createElement("div");
		problemNameDiv.classList.add("h5");
		problemNameDiv.insertAdjacentHTML('afterbegin', solutions[i].problemName);

		//Appending Children of Header div
		solutionHeaderDiv.appendChild(platformP);
		solutionHeaderDiv.appendChild(problemNameDiv);

		var cardLeftHeaderSeparator = document.createElement("hr");

		var languagesContainer = document.createElement("div");
		languagesContainer.classList.add("languages-container-margins");
		languagesContainer.classList.add("text-center")

		//language
		for(var j = 0; j < solutions[i].languages.length; j++){

			var linkCode = document.createElement("a");
			linkCode.classList.add("language-icon");
			linkCode.setAttribute('target', '_blank');
			linkCode.setAttribute('title', languages[solutions[i].languages[j]]);
			linkCode.setAttribute('href', 'codelibrary/' + solutions[i].problemName.toLowerCase().replaceAll(" ", "") + solutions[i].languages[j] + ".html")

			var icon = document.createElement("i");
			icon.classList.add("devicon-" + languages[solutions[i].languages[j]].toLowerCase() + "-plain");

			linkCode.appendChild(icon);

			languagesContainer.appendChild(linkCode);
		}

		//APPENDING CHILDREN FOR THE FIRST COLUMN
		colMd3.appendChild(solutionHeaderDiv);
		colMd3.appendChild(cardLeftHeaderSeparator);
		colMd3.appendChild(languagesContainer);

		var colMd9 = document.createElement("div");
		colMd9.classList.add("col-md-9");

		var cardBodyDiv = document.createElement("div");
		cardBodyDiv.classList.add("card-body");

		var problemDescriptionP = document.createElement("p");
		problemDescriptionP.insertAdjacentHTML('afterbegin',solutions[i].problemDescription) // change this later after insert the problem description to the array

		var linkDescription = document.createElement("a");
			linkDescription.classList.add("btn");
			linkDescription.classList.add("btn-primary");
			linkDescription.setAttribute('target', '_blank');
			linkDescription.setAttribute('href',solutions[i].descriptionLink)
			linkDescription.insertAdjacentHTML('afterbegin',"More Details and Examples");

		//appending children for cardBodyDiv
		cardBodyDiv.appendChild(problemDescriptionP);
		cardBodyDiv.appendChild(linkDescription);

		colMd9.appendChild(cardBodyDiv);

		rowDiv.appendChild(colMd3);
		rowDiv.appendChild(colMd9);

		cardDiv.appendChild(rowDiv);

		unorderedList.appendChild(cardDiv);
	}
}

(function() {

	solutions.sort(solutionsCompare);

	generateSolutionsList()
})();